# Type casting or Type conversion:- Converting one data type to another data type.

# Types of conversion
# 1. str()
# 2. int()
# 3. bool()
# 4. float()
# 5. list()

# str to int
oldAge = input("Enter your age? ")
newAge = int(oldAge) + 2
print(newAge)

# float to int
gravity = 9.77
print(int(gravity))

# str to list
word = "hello"
print(word)
wordTolist = list(word)
print(list(word))
print(wordTolist)