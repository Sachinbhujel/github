# Addition(+)
print(5 + 7)

# Subtraction(-)
print(87 - 11)

# Multiplication(*)
print(34 * 2)

# Division(/)
print(5 / 2)

# Floor division
print(5 // 2)

# Modules
print(5 % 2)

# Exponential or Power(**)
print(5 ** 2)
print(5 ** 3)

# Shortcuts for arithmetic operations
num = 5
num = num + 3
num += 3 # same as num = num + 3
num -= 2 # same as num = num - 2
#num /= 5 # same as num =  num / 5
#num *= 5 # same as num = num * 5
num //= 2 # same as num =  num // 2
num **= 2 # same as num = num ** 2
print(num)